import SwiftUI

extension Color {
    /// A global accent color set to FFC000 (RGB: 255, 192, 0)
    static let accentColor = Color(red: 1.0, green: 192/255, blue: 0)
    
    /// A global secondary brand color set to F0F0F0 (RGB: 240, 240, 240)
    static let brandGray = Color(red: 240/255, green: 240/255, blue: 240/255)
}
